/**
 * The default values interface of the figure
 */
public interface FigDefaultValues {
  /**
   * the pen thickness
   */
  double THICKNESS = 0.20;
  /**
   * the canvas resolution
   */
  double RESOLUTION = 0.05;
  /**
   * the pen color
   */
  String COLOR = "#000000";
  /**
   * the fill color
   */
  String FILL = "#FFFFFF";
}
